package com.example.whensaturdaycome.persuratan.data;

public class Informa {

    String id_informasi, id_user, judul, waktu, isi;

    public String getId_informasi() {
        return id_informasi;
    }

    public void setId_informasi(String id_informasi) {
        this.id_informasi = id_informasi;
    }

    public String getId_user() {
        return id_user;
    }

    public void setId_user(String id_user) {
        this.id_user = id_user;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getIsi() {
        return isi;
    }

    public void setIsi(String isi) {
        this.isi = isi;
    }
}
